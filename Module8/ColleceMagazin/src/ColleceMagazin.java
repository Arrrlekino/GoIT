package ua.arlekinoo.lecture8;
public class ColleceMagazin {
    private String musicalInstrument;
    private String color;
    private double price;

    public ColleceMagazin(final String musicalInstrument, final String color, final double price){
        this.musicalInstrument = musicalInstrument;
        this.color = color;
        this.price = price;
    }

    public String getMusicalInstrument() {
        return musicalInstrument;
    }

    public String getColor() {
        return color;
    }

    public double getPrice() {
        return price;}
}
